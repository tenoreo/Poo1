    private void btnALicenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnALicenciaActionPerformed
        String[] recursoNuevo=new String[4];
        recursoNuevo[0]=tfNumero.getText().trim();
        recursoNuevo[1]=(String) cbbTipo.getSelectedItem();
        recursoNuevo[2]=tfFEmision.getText();
        recursoNuevo[3]=tfFExpiracion.getText();
        boolean pase=true;
        if(recursoNuevo[0].equals("") && recursoNuevo[1].equals("") && recursoNuevo[2].equals("") && recursoNuevo[3].equals("")){
            JOptionPane.showMessageDialog(this,"Por favor ingrese todos los datos para una licencia");// TODO add your handling code here:
            pase=false;}
        for(int i=0;i<tabla.getRowCount();i++){
            if(list.get(i).equals(recursoNuevo[0].toLowerCase()) && 
                    list.get(i).equals(recursoNuevo[1].toLowerCase())){
                JOptionPane.showMessageDialog(this,"Ya ingresastes la misma licencia");
                pase=false;
            } else {
            }
        }
        if(pase){            
            tabla.addRow(recursoNuevo);
            list.add(recursoNuevo);
            tfNumero.setText("");
            cbbTipo.setSelectedIndex(0);
            tfFEmision.setText("");
            tfFExpiracion.setText("");
        }
    }//GEN-LAST:event_btnALicenciaActionPerformed

