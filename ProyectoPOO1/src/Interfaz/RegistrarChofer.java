    private void btnRChoferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRChoferActionPerformed
        String nombre=tfNombre.getText();
        String cedula=tfCedula.getText();
        String correo=tfCorreo.getText();
        String telefono=tfTelefono.getText();
        if(!validarCorreo(correo))
            JOptionPane.showMessageDialog(this,"Este correo no es valido");
        else if(tryParse(cedula)<1)
            JOptionPane.showMessageDialog(this,"La cedula debe ser solo enteros");
        else if(tryParse(telefono)<1)
            JOptionPane.showMessageDialog(this,"El telefono debe ser solo enteros");
        else if(list.size()==0)
            JOptionPane.showMessageDialog(this,"El conductor debe tener licencias");
        else{
            Xml nuevoXml=new Xml();
            String datos[]=new String[5];
            datos[0]=nombre;
            datos[1]=cedula;
            datos[2]=correo;
            datos[3]=telefono;
            String[] licencias=new String[list.size()];
            for(int i=0;i<list.size();i++)
                licencias[i]=list.get(i);
        }
    }//GEN-LAST:event_btnRChoferActionPerformed

